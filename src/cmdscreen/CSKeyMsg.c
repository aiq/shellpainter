static inline void cleanup( void* instance ) {}

cMeta const CS_KeyMsg = {
   .desc = stringify_c_( CS_KeyMsg ),
   .cleanup = &cleanup
};

/*******************************************************************************
********************************************************************* Functions
********************************************************************************

*******************************************************************************/

bool write_key_msg_cs( cRecorder rec[static 1],
                       CSKeyMsg const* msg,
                       char const fmt[static 1] )
{
   must_exist_c_( msg );

   return write_c_( rec, "key msg ",
                    "=> dec:{u16}", msg->code,
                    " / hex:{u16:x}", msg->code,
                    " / run:{r}", msg->rune );
}
