#include "uiinabox/none.h"

#include "uiinabox/_/util.h"

/*******************************************************************************
********************************************************* Types and Definitions
********************************************************************************
 type
*******************************************************************************/

static bool do_none( uiBox box[static 1],
                     uiLimit limit,
                     cErrorStack es[static 1] )
{
   box->rect.w = 0;
   box->rect.h = 0;
   return true;
}
uiBoxType const UI_None = {
   .desc = "none",
   .layout = &do_none
};

/*******************************************************************************
********************************************************************* Functions
********************************************************************************

*******************************************************************************/

uiBox none_ui( void )
{
   return (uiBox){
      .data=NULL,
      .type=&UI_None,
      .content=NULL,
      .children=(csVarBoxes){ .s=0, .v=NULL }
   };
}
